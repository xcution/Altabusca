<?php


// retorna o numero de itens do array de post -----

function retorne_numero_itens_array_post(){


// contador --------------------------------------------------

$contador = 0; // contador

// --------------------------------------------------------------


// valida itens de array -----------------------------------

foreach($_POST as $key=>$value){


// valida valor e atualiza contador ---------------------

if($value != null){

$contador++; // atualizando contador

};

// --------------------------------------------------------------


};

// --------------------------------------------------------------


// retorno ----------------------------------------------------

return $contador; // retorno

// --------------------------------------------------------------


};

// --------------------------------------------------------------


?>